public class PrintIntro {
    public static void main(String[] args) {
        // PRINTING
        System.out.println("hello world"); // println creates a new line after printing
        System.out.print("hello keerti"); // printed on a new line
        System.out.println("sup"); // on the same line as previous bc a new line was not created

        // COMMENTING
        // a comment, which is not run and used to annotate code

        // ESCAPE SEQUENCES
        //      \" (print quotation)
        System.out.println("green is \"cool\"");
        //      \n (starts new line)
        System.out.println("k\no\ny\na");
        //      \t (prints a tab)
        System.out.println("k\to\ty\ta");
        //      \\ (prints backslash)
        System.out.println("k\\o\\y\\a");

        // EMPTY SOUT (empty line)
        System.out.println("part 1");
        System.out.println();
        System.out.println("part 2");

    }
}
